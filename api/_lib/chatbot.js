/**
 * Groq কে কল করে রিপ্লাই নিয়ে আসে।
 * messages = [{ role: 'user'|'assistant', content: '...' }, ...]
 */
async function getGroqReply(messages) {
  const apiKey = process.env.GROQ_API_KEY;
  if (!apiKey) {
    throw new Error('সার্ভারে GROQ_API_KEY সেট করা নেই (Vercel → Settings → Environment Variables)');
  }

  const res = await fetch(GROQ_ENDPOINT, {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${apiKey}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      model: GROQ_MODEL,
      messages: [{ role: 'system', content: SYSTEM_PROMPT }, ...messages],
      temperature: 0.7,
      max_tokens: 800
    })
  });

  const data = await res.json();
  if (!res.ok) {
    throw new Error(data?.error?.message || `Groq API error (status ${res.status})`);
  }

  return data.choices?.[0]?.message?.content || 'দুঃখিত, উত্তর পাওয়া যায়নি।';
}

module.exports = { SYSTEM_PROMPT, getGroqReply };

